class LegalRepresentative {
  constructor(
    id,
    provider_id,
    identification_type,
    identification_number,
    full_name,
    email
  ) {
    this.id = id;
    this.provider_id = provider_id;
    this.identification_type = identification_type;
    this.identification_number = identification_number;
    this.full_name = full_name;
    this.email = email;
  }
}
